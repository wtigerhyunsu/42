int	ft_str_is_numeric(char *str)
{
	char a;

	if (*str != '\0')
	{
		while (*str != '\0')
		{
			a = *str++;
			if ((a < '0') || (a > '9'))
			{
				return (0);
			}
		}
	}
	return (1);
}
