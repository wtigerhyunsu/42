char	*ft_strupcase(char *str)
{
	char a;
	char *pint;

	pint = str;
	while (*str != '\0')
	{
		a = *str;
		if (a >= 'a' && a <= 'z')
		{
			*str -= 32;
		}
		str++;
	}
	return (pint);
}
