char	*ft_strlowcase(char *str)
{
	char *pint;
	char a;

	pint = str;
	while (*str != '\0')
	{
		a = *str;
		if (a >= 'A' && a <= 'Z')
		{
			*str += 32;
		}
		str++;
	}
	return (pint);
}
