#include <stdio.h>

int	ft_str_is_alpha(char *str)
{
	char a;

	if (*str != '\0')
	{
		while (*str != '\0')
		{
			a = *str++;
			if ((a >= 'a' && a <= 'z') || (a >= 'A' && a <= 'Z'))
			{
				continue ;
			}
			return (0);
		}
	}
	return (1);
}
