int				ft_sqrt(int nb)
{
	long long	ret;

	ret = 1;
	if (nb == 1)
		return (1);
	while (ret * ret < nb)
	{
		ret++;
		if (ret * ret == nb)
			return (ret);
	}
	return (0);
}
